// write a function checkNumber(num) that returns:
// ..."Positive" if num > 0
// ..."Negative" if num < 0
// ..."Zero" if the num === 0
function checkNumber (num){
    return num > 0? "Positive" : num < 0? "Negative" : "Zero"
}
console.log(checkNumber(10));
console.log(checkNumber(-7));
console.log(checkNumber(0));




